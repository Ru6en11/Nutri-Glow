class AnthropometricData {
  final String userId;
  final String gender;
  final int age;
  final int weight;
  final int height;
  final String activityLevel;
  final String goal;

  const AnthropometricData({
    required this.userId,
    required this.gender,
    required this.age,
    required this.weight,
    required this.height,
    required this.activityLevel,
    required this.goal,
  });
}
